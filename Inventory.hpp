#ifndef INVENTORY_HPP
#define INVENTORY_HPP

//  Inventory.hpp

#include <vector>

#include "Item.hpp"


namespace inp{
    class INFPaket;
};


class Inventory{
    public:
        friend class InventoryBox;

        static const int revision_ = 1;

        Inventory();
        ~Inventory();

        void clear();
        bool empty()const;
        size_t size()const;
        const Item& at(size_t i)const;

        void setMaxSize(int size);
        int getMaxSize()const;

        void gainItem(const Item& newItem);
        void removeItem(size_t listIndex);

        bool savePacket(inp::INFPacket& pack)const;
        bool load(inp::INFPacket &pack);


    protected:
        static const int startSize_ = 32;
        int maxSize_;
        typedef std::vector<Item> ItemList;
        ItemList list_;
};


#endif
