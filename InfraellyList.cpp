//  InfraellyList.cpp

#include "InfraellyList.hpp"


using namespace std;

bool InfraellyList::empty()const{ return contents.empty(); }

int InfraellyList::addElement(const std::string& item){
    contents.push_back(item);
    return contents.size()-1;
}


void InfraellyList::removeElementAt(size_t i){
    if( (i > 0) && (i < contents.size()) ){
        contents.erase(contents.begin()+i);
    };
}




int InfraellyList::getNumberOfElements(){
    return contents.size();
}


std::string InfraellyList::getElementAt(int i){
    if( (i > -1) && (i < contents.size()) ){
        return contents.at(i);
    } else {
        return "Index out of Bounds";
    }
}
