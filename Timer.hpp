#ifndef TIMER_HPP_INCLUDED
#define TIMER_HPP_INCLUDED


#include <SDL/SDL.h>



class Timer{
    public:
        Timer();
        ~Timer();


        //  Starts the Timer
        void start();

        //  Stops Timer
        void stop();

        //  Clears Time
        //  If stop was not called, timeing will continue from this point
        void clear();


        //  Returns how long the timer has timed for. (ie check time)
        long getTime()const;
        //  Returns whether the timer is timing or not
        bool isTiming()const;



    protected:
        bool timing;            //  Whether the time is started or not
        long startTime;         //  The time the timer was started
        long timeElapsed;       //  Stores time when timer is paused
};





#endif // TIMER_HPP_INCLUDED
