#ifndef INFOFORM_HPP
#define INFOFORM_HPP

//  InfoForm.hpp

#include <string>

#include "guichan.hpp"
#include "InfraellyWindow.hpp"


class InfoForm : public InfraellyWindow {
    public:
        InfoForm();
        virtual ~InfoForm();

        //  Call this to let other pieces of code to not use the info
        //      (you have alredy used it)
        void infoTaken();

        //  Returns true if there is valid input in fields
        bool isInfoReady()const;

        virtual void positionDefaultBtns();

        //Called when corresponding button is pressed
        virtual void doneAction() = 0;
        virtual void cancelAction() = 0;

        //mouse listener
        virtual void mouseClicked(gcn::MouseEvent& mouseEvent);


    protected:
        gcn::Button *doneButton;
        gcn::Button *cancelButton;

        bool infoReady;
};


#endif
