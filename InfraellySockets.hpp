#ifndef INFRAELLYSOCKETS_HPP_INCLUDED
#define INFRAELLYSOCKETS_HPP_INCLUDED

#include <iostream>

#ifdef WIN32
    #include <winsock.h>
#endif

namespace inp{

    inline void initNet(){
        #ifdef WIN32
        WSADATA wsaData;
        if(WSAStartup(MAKEWORD(1, 1), &wsaData) != 0){
            std::cerr << __FILE__ << __LINE__ << ": WSAStartup failed." << std::endl;
            exit(EXIT_FAILURE);
        }
        #endif
    }

    inline int quitNet(){
        #ifdef WIN32
        return WSACleanup();
        #endif
    }


    inline int close(int s){
        #ifdef WIN32
        return closesocket(s);
        #endif
    }
}




#endif // INFRAELLYSOCKETS_HPP_INCLUDED
