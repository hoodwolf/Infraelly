#ifndef PROGRESSBAR_HPP
#define PROGRESSBAR_HPP

//  ProgressBar.hpp

#include <string>
#include "guichan.hpp"

class ProgressBar : public gcn::Widget{
    protected:
        enum ProgressMode{ FRACTION, PERCENTAGE, NONE }mode;

    public:
        ProgressBar();
        ProgressBar(const std::string& prefix, float progress, float totalAmount);
        ~ProgressBar();

        enum ProgressMode getProgressMode()const;
        bool isShowText()const;
        std::string getPrefix()const;
        float getProgress()const;
        float getTotal()const;

        void setProgressMode(enum ProgressMode mode);
        void setShowText(bool showText);
        void setPrefix(const std::string& newPrefix);
        void setProgress(float progress);
        void setTotal(float total);

        virtual void draw(gcn::Graphics* graphics);

    protected:
        bool showText;

        std::string prefix;

        float progress;
        float total;
};




#endif
