#ifndef CACHEABLE_HPP
#define CACHEABLE_HPP

//******************************************************************************
//
//                                Cacheable
//
//  This is an abstract class which is used by the ResourceCache class. For
//  a resource to be stored in a ResourceCache, it be must derrived from this
//  class.
//
//******************************************************************************

#include <string>


class Cacheable {
    public:
        Cacheable(){};
        virtual ~Cacheable(){};

        //  This function should load data form a file into the local object,
        //  It should also set "filename_" to the filename that was loaded
        //  ie.
        //      Cacheble foo;
        //      foo.cache_load("foo.dat");
        //  foo should now contain the data from foo.dat
        //  Return false on fail, true on success
        virtual bool cache_load( const std::string& filename ) = 0;

        void setFilename(const std::string& newFn){ filename_ = newFn; }
        const std::string& getFilename()const{ return filename_; }


    protected:
        std::string filename_;
};


#endif
