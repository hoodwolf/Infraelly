#ifndef INFRAELLYWINDOW_HPP
#define INFRAELLYWINDOW_HPP

//  InfraellyWindow.hpp

#include "guichan.hpp"


class InfraellyWindow : public gcn::Window {
    public:
        InfraellyWindow();
        virtual ~InfraellyWindow();

        // overloaded to place the window controls properly
        virtual void resizeToContent();

        //  repositions the collapse and hide buttons
        void repositionControls(bool adjustHeight = false);

        //  returns  the height of the collapse and hide buttons
        int getViewControllsHeight()const;

        //  removes the hide and collapse buttons
        void removeViewControls();
        //  adds the hide and collapse buttons
        void addViewControls();

        virtual void mouseClicked(gcn::MouseEvent& mouseEvent);
        virtual void mouseReleased(gcn::MouseEvent& mouseEvent);
        virtual void draw(gcn::Graphics* graphics);


    protected:
        bool controlsAttached_;
        bool collapsed_;
        int oldHeight_;

        gcn::Button *collapseBtn_;
        gcn::Button *hideBtn_;
};




#endif
